alert("Volume caixa retângular");
var altura = parseInt(prompt("Digite o valor da altura da caixa"));
var largura = parseInt(prompt("Digite o valor da largura da caixa"));
var comprimento = parseInt(prompt("Digite o valor do comprimento da caixa"));
volume = altura * largura * comprimento;
alert(`O valor do volume da caixa retângular é: ${volume} cm³`);
